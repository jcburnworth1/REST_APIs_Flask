##### Booleans #####
## Comparisons: ==, !=, >, <, >=, <=
print(5 == 5)
print(5 > 5)
print(10 != 10)

##### Ease Keyword #####
friends = ['Bob', 'Rolf']
abroad = ['Rolf', 'Bob']

print(friends == abroad) ## Compares if elements inside are the same
print(friends is abroad) ## Compares actual same thing (same memory location)

